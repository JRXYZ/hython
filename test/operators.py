# Floating point arithmetic
print(+5.0)
print(-5.0)
print(1.0 + 2.0)
print(2.0 - 0.5)
print(1.0 * 1.6)
print(1.0 / 4.8)
print(5.0 % 2.0)
print(5.0 // 2.0)
print(5.0 ** 3.4)
print(0.0 ** 0.0)

