# Boolean operations
print(42 == 42)
print(42 != 18)

# Integer boolean operations
print(5 > 1)
print(5 >= 5)
print(4 < 1)
print(4 <= 4)

# Float boolean operators
print(5.0 > 1.0)
print(4.0 < 1.0)
print(5.0 >= 5.0)
print(4.0 <= 4.0)

# Mixed int/float with operators
print(5 == 1.0)
print(4 != 1.0)
print(5 > 1.0)
print(4 < 1.0)
print(5 >= 5.0)
print(4 <= 4.0)

print(5.0 == 1)
print(4.0 != 1)
print(5.0 > 1)
print(4.0 < 1)
print(5.0 >= 5)
print(4.0 <= 4)

